int n = 1000;
int x = 10;
ll ans = 0;

void brute(int kzk, int left, int last){
    if(last > left) return;
    if(kzk == x){
        ans++;
        return;
    }
    for(int i = last; i + i <= left; i++){
        brute(kzk + 1, left - i, i);
    }
}

 
